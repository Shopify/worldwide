    def cardinal_key_for(locale, n)
      rule = Worldwide::Cldr.fallbacks[locale].detect do |fallback_locale|
        result = load_locale_rule(fallback_locale)
        break result if result
      end

      rule&.call(n)
    end

    def load_locale_rule(locale)
      locale_key = locale.to_s
      formatted_locale = locale.to_s.tr("-", "_")
      plural_rules_path = File.expand_path(File.join(Paths::CLDR_ROOT, "locales", locale_key, "plurals.rb"))
      return unless File.exist?(plural_rules_path)

      begin
        # rubocop:disable Security/Eval
        # I agree that the following line is not pretty.  But it's internally-provided code, so not absolutely unsafe.
        rules = eval(File.read(plural_rules_path)) # TODO: Oh god! eval
        # rubocop:enable Security/Eval
        rules.dig(formatted_locale.to_sym, :i18n, :plural, :rule)
      rescue SyntaxError
        nil
      end
    end
